package EjerCap4;


public class Ej7Persona {

	private String nombre;
    private String dirección;

    public Ej7Persona(String nombre, String dirección) {
        this.nombre = nombre;
        this.dirección = dirección;
    }

    public String getNombre() {
        return nombre;
    }

    public String getDirección() {
        return dirección;
    }

}
